package org.wahlzeit.model;

/**
 * A coordinate represents the x,y,z coordinate of a location.
 */
public class Coordinate {

    /**
	 * cartesian x, y, z coordinates
	 */
    private double x;
    private double y;
    private double z;

	/**
	 * 
	 */
	public Coordinate() {
		x = 0;
        y = 0;
        z = 0;
	}

}
